////////////////////////////////////////////////////////////////////////////////

function countChars(str, character) {
  var count = 0;
  if(str.length<=1){
    return 0;
  }else{
    for(var i =0; i< str.length; i++){
      if(str[i] === character){
        count++;
      }
    }
  }
  return count;
}

////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countBs(str) {
  var count = 0;
  if(str.length<=1){
    return 0;
  }else{
    for(var i =0; i< str.length; i++){
      if(str[i] === 'b' || str[i] === "B"){
        count++;
      }
    }
  }
  return count;

}

